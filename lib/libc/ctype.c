int isalnum(int c) {
	return (global_ctype[c].flags & (_CTYPE_ALPHA | _CTYPE_DIGIT));
}
int isalpha(int c) {
	return (global_ctype[c].flags & _CTYPE_ALPHA);
}
int isblank(int c) {
	return (global_ctype[c].flags & _CTYPE_BLANK);
}
int iscntrl(int c) {
	return (global_ctype[c].flags & _CTYPE_CNTRL);
}
int isdigit(int c) {
	return (global_ctype[c].flags & _CTYPE_DIGIT);
}
int isgraph(int c) {
	return (global_ctype[c].flags & _CTYPE_GRAPH);
}
int islower(int c) {
	return (global_ctype[c].flags & _CTYPE_LOWER);
}
int isprint(int c) {
	return (global_ctype[c].flags & _CTYPE_GRAPH) || (c == ' ');
}
int ispunct(int c) {
	return (global_ctype[c].flags & _CTYPE_PUNCT);
}
int isspace(int c) {
	return (global_ctype[c].flags & _CTYPE_SPACE);
}
int isupper(int c) {
	return (global_ctype[c].flags & _CTYPE_UPPER);
}
int isxdigit(int c) {
	return (global_ctype[c].flags & _CTYPE_XDIGT);
}
int tolower(int c) {
	return global_ctype[c].lower;
}
int toupper(int c) {
	return global_ctype[c].upper;
}

